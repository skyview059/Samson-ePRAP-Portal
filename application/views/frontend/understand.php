        <h2>How do examiners mark the exam?</h2>
        <p>Each station will be assessed based on your performance against the three marking domains in it: </p>
        <ul>
            <li>Data gathering, technical and assessment skills</li>
            <li>Clinical management skills </li>
            <li>Interpersonal skills.</li>
        </ul>
        <h2>Is there a fixed pass mark for each station?</h2>
        <p>No, each station has a pass mark which uses the borderline regression scoring method. This means the pass mark varies with the difficulty of the station and for each exam.</p>
        <h2>Does the examiner know the station pass mark?</h2>
        <p>No, the examiner does not know the pass mark when assessing candidates.</p>
        <h2>How do we work out if candidates meet the required standard of the exam?</h2>
        <p>We add up the pass mark for each of the 18 stations in the exam. We then add one standard error of measurement. This creates the total score for the exam. You must meet or exceed the total score and also pass a minimum of 11 stations to achieve an overall pass.</p>
        <p><span style="background: white; color: #4a4a4a;">We may change our standard setting procedures from time to time without notice. As a part of our quality assurance procedures we may also exclude individual stations from your results. When we do this we will reduce the pass mark and the minimum number of stations needed to pass accordingly to ensure that you are not disadvantaged.</span></p>
        <p><span style="background: white; color: #4a4a4a;">For example, if one station is excluded, results will be calculated from 17 stations only. Therefore, 10 stations would be required to pass the examination as well as meeting the pass mark.</span></p>
